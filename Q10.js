
// What are lambda expression or arrow functions?

// Lambda expressions, also known as arrow functions in JavaScript, are a concise way to write functions.
// Lambda expressions, also known as arrow functions in JavaScript, are a concise way to define anonymous functions. They are particularly common in languages that support functional programming paradigms, such as JavaScript.


// Anonymous Functions: Lambda expressions allow you to define functions without a name.Instead of using the function keyword to define a named function, lambda expressions provide a shorter syntax.

// Example
const numbers = [1, 2, 3, 4, 5];
const doubledNumbers = numbers.map((x) => x * 2);
console.log(doubledNumbers); // Output: [2, 4, 6, 8, 10]

