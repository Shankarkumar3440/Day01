// What is a first order function?
//  "first-order function" refers to a function that does not take other functions as arguments and does not return functions as results


// Only operate on data values like numbers, strings, arrays, objects, etc.
// Do not accept other functions as parameters.
// Do not return functions.


function add(a, b) {
    return a + b;
}

const result = add(2, 3); // result is 5

